    private void jupdatebActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jupdatebActionPerformed
        String id =jTextField1.getText();
        String name =jTextField2.getText();
        String opt1 =jTextField3.getText();
        String opt2 =jTextField4.getText();
        String opt3 =jTextField5.getText();
        String opt4 =jTextField6.getText();
        String answer =jTextField7.getText();
        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement(
                "update question set name=?,opt1=?,opt2=?,opt3=?,opt4=?,answer=? where id=?");
            ps.setString(1, name);
            ps.setString(2, opt1);
            ps.setString(3, opt2);
            ps.setString(4, opt3);
            ps.setString(5, opt4);
            ps.setString(6, answer);
            ps.setString(7, id);
            ps.executeUpdate();
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, "Successfully Updated");
            setVisible(false);
            new updateQuestion().setVisible(true);
        }
        catch (Exception e) {
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, e);
        }
    }//GEN-LAST:event_jupdatebActionPerformed

